    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String startDate = ((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText();
        String endDate = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/stock" , "root" , "1234abcD.");
            String query = "SELECT SUM(earnings) AS total FROM vente WHERE date BETWEEN ? AND ?";
            PreparedStatement statement = conn.prepareStatement(query);
            statement.setString(1,startDate);
            statement.setString(2, endDate);
            ResultSet res = statement.executeQuery();
             while (res.next()) {
                float earnings = res.getFloat("total");
                jLabel4.setText(Float.toString(earnings));
             }
            String query2 = "SELECT SUM(prix) AS ventes FROM vente WHERE date BETWEEN ? AND ?";
            statement = conn.prepareStatement(query2);
            statement.setString(1,startDate);
            statement.setString(2, endDate);
            ResultSet result = statement.executeQuery();
             while (result.next()) {
                float ventes = result.getFloat("ventes");
                jLabel7.setText(Float.toString(ventes));
             }
            tableModel.setRowCount(0);
            String query3 = "SELECT nom, COUNT(codebarre) AS quantite, SUM(earnings) AS earning FROM vente WHERE date BETWEEN ? AND ? GROUP BY nom";
            statement = conn.prepareStatement(query3);
            statement.setString(1,startDate);
            statement.setString(2, endDate);
            ResultSet res1 = statement.executeQuery();
             while (res1.next()) {
                    Object[] rowData = {
                            res1.getString("nom"),
                            res1.getInt("quantite"),
                            res1.getFloat("earning")
                        };
                    tableModel.addRow(rowData);
             }
        } catch (SQLException ex) {
            Logger.getLogger(Statistiques.class.getName()).log(Level.SEVERE, null, ex);
            StringWriter sw = new StringWriter();
            PrintWriter pw = new PrintWriter(sw);
            ex.printStackTrace(pw);
            Erreur.erreur = sw.toString();
            new Erreur(null,true).show();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

